resolve_package_signing_mode() {
  local requested="${CODEXBAR_SIGNING:-adhoc}"
  case "$requested" in
    adhoc|identity) ;;
    *)
      echo "ERROR: Unsupported CODEXBAR_SIGNING: $requested (expected adhoc or identity)" >&2
      return 1
      ;;
  esac
  SIGNING_MODE="$requested"
}

